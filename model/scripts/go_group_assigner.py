"""
GO Term Group Assigner
=======================
Assigns each GO term in your dataset to exactly ONE group using
a strict "most specific ancestor" rule.

How the assignment rule works:
    A GO term may have ancestors in multiple top-level groups (because GO is a DAG).
    To avoid overlap, we assign each term to the group whose root is the SMALLEST
    (most specific / deepest in hierarchy). This means we always prefer the more
    precise biological category over a broader one.

Usage:
    1. Make sure go_dict.json is in the same directory (generated by go_parser.py)
    2. Set DATASET_PATH and GO_COL_IDX below
    3. Run: python go_group_assigner.py
"""

